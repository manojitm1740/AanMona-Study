export type Language = 'en' | 'bn';

/**
 * Helper function to translate numerals to Bengali if active language is Bengali
 */
export function formatNumber(num: number | string, lang: Language): string {
  const numStr = String(num);
  if (lang === 'en') return numStr;

  const bengaliNumerals: Record<string, string> = {
    '0': '০', '1': '১', '2': '২', '3': '৩', '4': '৪',
    '5': '৫', '6': '৬', '7': '৭', '8': '৮', '9': '৯'
  };

  return numStr.split('').map(char => bengaliNumerals[char] || char).join('');
}
